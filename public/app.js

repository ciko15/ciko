// Initialize Map with theme support
function initMap() {
  if (map) return;
  
  const theme = document.documentElement.getAttribute('data-theme') || 'dark';
  
  // Tile URLs for different themes
  const tileUrls = {
    dark: 'https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png',
    light: 'https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png'
  };
  
  // Initialize Leaflet map centered on Indonesia
  map = L.map('mapContainer', {
    zoomControl: true,
    attributionControl: true
  }).setView([-2.5, 118], 5);
  
  // Add tiles based on current theme
  mapTileLayer = L.tileLayer(tileUrls[theme], {
    attribution: theme === 'dark' ? '&copy; CARTO' : '&copy; OpenStreetMap',
    maxZoom: 19
  }).addTo(map);
  
  // Add airport markers
  updateMapMarkers();
}

// Update Map Markers with shadow effect (no white border)
function updateMapMarkers() {
  if (!map) return;
  
  // Clear existing markers
  map.eachLayer(layer => {
    if (layer instanceof L.Marker) {
      map.removeLayer(layer);
    }
  });
  
  // Get current theme
  const theme = document.documentElement.getAttribute('data-theme') || 'dark';
  
  // Custom icons with glow/shadow effect (no white border)
  const createIcon = (color, status) => {
    const glowColor = color + '60'; // Add transparency for glow
    return L.divIcon({
      className: 'custom-marker-container',
      html: `<div style="
        background: ${color};
        width: 18px;
        height: 18px;
        border-radius: 50%;
        box-shadow: 0 0 8px ${glowColor}, 0 2px 6px rgba(0,0,0,0.4);
        cursor: pointer;
        ${status === 'Alert' ? 'animation: pulse 1.5s infinite;' : ''}
      "></div>`,
      iconSize: [18, 18],
      iconAnchor: [9, 9]
    });
  };
  
  const icons = {
    Normal: createIcon('#10b981', 'Normal'),
    Warning: createIcon('#f59e0b', 'Warning'),
    Alert: createIcon('#ef4444', 'Alert')
  };
  
  // Popup text color based on theme
  const textColor = theme === 'dark' ? '#eaeaea' : '#1a1a2e';
  const mutedColor = theme === 'dark' ? '#a0a0a0' : '#6b7280';
  
  // Add markers for each airport
  airportsData.forEach(airport => {
    const marker = L.marker([airport.lat, airport.lng], {
      icon: icons[airport.status]
    }).addTo(map);
    
    const popupContent = `
      <div class="popup-airport" style="min-width: 180px; font-family: 'Segoe UI', sans-serif; color: ${textColor};">
        <h3 style="font-size: 1rem; margin-bottom: 6px; color: ${textColor};">${airport.name}</h3>
        <p style="font-size: 0.85rem; margin-bottom: 8px; color: ${mutedColor};">${airport.city}</p>
        <span style="
          padding: 3px 8px; 
          border-radius: 10px; 
          font-size: 0.7rem; 
          font-weight: 500;
          background: ${airport.status === 'Normal' ? 'rgba(16, 185, 129, 0.2)' : airport.status === 'Warning' ? 'rgba(245, 158, 11, 0.2)' : 'rgba(239, 68, 68, 0.2)'}; 
          color: ${airport.status === 'Normal' ? '#10b981' : airport.status === 'Warning' ? '#f59e0b' : '#ef4444'};
        ">
          ${airport.status}
        </span>
        <div style="margin-top: 8px; font-size: 0.75rem; color: ${mutedColor};">
          <p>Total: ${airport.totalEquipment || 0}</p>
        </div>
      </div>
    `;
    
    let popupOpen = false;
    
    marker.on('mouseover', function(e) {
      this.openPopup();
      popupOpen = true;
    });
    
    marker.on('mouseout', function(e) {
      setTimeout(() => {
        if (!popupOpen) {
          this.closePopup();
        }
      }, 200);
    });
    
    marker.bindPopup(popupContent, {
      closeButton: false,
      keepInView: true,
      className: theme === 'dark' ? 'dark-popup' : 'light-popup'
    });
    
    marker.on('popupopen', function() {
      const popup = this.getPopup();
      const popupElement = popup.getElement();
      if (popupElement) {
        popupElement.addEventListener('mouseenter', () => { popupOpen = true; });
        popupElement.addEventListener('mouseleave', () => { popupOpen = false; this.closePopup(); });
      }
    });
  });
}

