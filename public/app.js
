// Theme Management
function initTheme() {
  const savedTheme = localStorage.getItem('theme') || 'dark';
  document.documentElement.setAttribute('data-theme', savedTheme);
  updateThemeButton(savedTheme);
}

function toggleTheme() {
  const currentTheme = document.documentElement.getAttribute('data-theme');
  const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
  document.documentElement.setAttribute('data-theme', newTheme);
  localStorage.setItem('theme', newTheme);
  updateThemeButton(newTheme);
  
  // Update map tiles if map exists
  if (map) {
    updateMapTiles(newTheme);
  }
}

function updateMapTiles(theme) {
  const tileUrls = {
    dark: 'https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png',
    light: 'https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png'
  };
  
  if (mapTileLayer) {
    map.removeLayer(mapTileLayer);
  }
  
  mapTileLayer = L.tileLayer(tileUrls[theme], {
    attribution: theme === 'dark' ? '&copy; CARTO' : '&copy; OpenStreetMap',
    maxZoom: 19
  }).addTo(map);
  
  // Update markers for theme
  updateMapMarkers();
}

function updateThemeButton(theme) {
  const icon = themeToggle.querySelector('i');
  const text = themeToggle.querySelector('span');
  const settingsToggle = settingsThemeToggle;
  
  if (theme === 'dark') {
    icon.className = 'fas fa-moon';
    text.textContent = 'Dark Mode';
    settingsToggle.classList.remove('active');
  } else {
    icon.className = 'fas fa-sun';
    text.textContent = 'Light Mode';
    settingsToggle.classList.add('active');
  }
}

// Navigation
function initNavigation() {
  navItems.forEach(item => {
    item.addEventListener('click', (e) => {
      e.preventDefault();
      const section = item.dataset.section;
      
      // Update active nav item
      navItems.forEach(nav => nav.classList.remove('active'));
      item.classList.add('active');
      
      // Show corresponding section
      contentSections.forEach(sec => sec.classList.add('hidden'));
      document.getElementById(`${section}Section`).classList.remove('hidden');
      
      // Close sidebar on mobile
      if (window.innerWidth <= 768) {
        sidebar.classList.remove('active');
      }
      
      // Resize map if dashboard section is shown
      if (section === 'dashboard' && map) {
        setTimeout(() => map.invalidateSize(), 100);
      }
    });
  });
}

